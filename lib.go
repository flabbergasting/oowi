package oowi

func Sum(a, b int) int {
    return a + b
}

func Mul(a, b int) int {
    return a * b
}

func Sub(a, b int) int {
    return a - b
}

func AAA(a, b int) int {
    return a + b * a
}

func BBB(a, b int) int {
    return a + b * b
}

func CCC (a, b, c int) int {
    return a + b + c * c
}
